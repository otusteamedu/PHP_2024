#!/bin/bash

if [ "$#" -lt 2 ]; then
    echo "Ошибка: необходимо передать два числа для сложения"
    exit 1
fi

if ! [[ "$1" =~ ^-?[0-9]+(\.[0-9]+)?$ ]] || ! [[ "$2" =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
    echo "Ошибка: аргументы должны быть числами"
    exit 1
fi

result=$(awk "BEGIN {print $1 + $2}")

echo "Результат сложения чисел $1 и $2: $result"