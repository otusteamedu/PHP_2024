#!/bin/bash

# Проверка количества аргументов
if [ "$#" -ne 2 ]; then
    echo "Ошибка: Необходимо передать два числа."
    exit 1
fi

# Проверка, что аргументы являются числами
if ! [[ "$1" =~ ^-?[0-9]+(\.[0-9]+)?$ ]] || ! [[ "$2" =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
    echo "Ошибка: Аргументы должны быть числами (целыми или вещественными)."
    exit 1
fi

# Суммирование чисел с использованием bc
sum=$(echo "$1 + $2" | bc)

# Вывод результата
echo "Сумма: $sum"